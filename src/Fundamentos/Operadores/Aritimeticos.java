package Fundamentos.Operadores;

public class Aritimeticos {

    public static void main(String[] args) {

        var x = 23.92;
        double y = 92.1;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);

        int a = 2;
        int b = 9;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a / (double) b);
        System.out.println(a / (float) b);
        System.out.println(a % b);

    }
}
